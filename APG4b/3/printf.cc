#include <bits/stdc++.h>
#include <cstdio>
using namespace std;

int main() {
  int x = 12345;
  double pi = 3.14159265358979;
  printf("x = %d, pi = %lf\n", x, pi);
  printf("%.10lf\n", pi);

  string s = "hello";
  printf("%s\n", s.c_str());

  int a = 1;
  int b = 99;
  int c = 123;
  printf("%03d, %03d, %03d\n", a, b, c);
}
